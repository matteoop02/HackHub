package unicam.ids.HackHub.enums;

public enum ReportType {
    VIOLATION,      // Segnalazioni di violazioni o comportamenti scorretti
    PERFORMANCE,    // Valutazioni sulle prestazioni del team
    COLLABORATION,  // Note su cooperazione e lavoro di squadra
    BEHAVIOR,       // Comportamento generale (positivo o negativo)
    TECHNICAL,      // Questioni tecniche, competenze o problemi procedurali
    ETHICS          // Questioni etiche, sicurezza o regole del hackathon
}
